/**
 * Generate the README tool catalog from the single source of truth: the
 * `toolDescriptors` array (compiled to dist/tools.js).
 *
 * Why this exists: MCPpedia (and similar registry scanners) extract a server's
 * tool list by reading the README with an LLM, slicing only the first ~8000
 * raw chars - they never run the server or parse the tarball. So the README's
 * first 8 KB must carry a complete, flat, parser-friendly list of every tool.
 * Hand-maintaining that drifts (the table once listed 18 while the array had
 * 71); deriving it here keeps it honest, and tests/tool-count.test.ts asserts
 * the rendered row-count equals toolDescriptors.length.
 *
 * Format rules (do not "improve" without re-checking the scanner regex):
 *   - Flat list, one tool per line: `- \`name\` - description`
 *   - Hyphen or colon separator only. NO em-dash (U+2014) - the fallback regex
 *     /[`*]+(\w+)[`*]+\s*[-:–]\s*(.+)/ rejects it, and it's off Miz's copy std.
 *   - All lines must land inside the first 8000 raw chars of the final README.
 *
 * Usage:
 *   node scripts/gen-readme-tools.mjs           # print block + byte report
 *   node scripts/gen-readme-tools.mjs --write    # rewrite the marked block in README.md
 *   node scripts/gen-readme-tools.mjs --write --details   # ...wrapped in <details>
 *
 * Run `npm run build` first so dist/tools.js is current.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath, pathToFileURL } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

const { toolDescriptors } = await import(
  pathToFileURL(join(ROOT, "dist", "tools.js")).href
).catch((err) => {
  console.error(`Could not load dist/tools.js (run \`npm run build\` first): ${err.message}`);
  process.exit(1);
});

const BEGIN = "<!-- BEGIN TOOL CATALOG (generated by scripts/gen-readme-tools.mjs from src/tools.ts - do not hand-edit) -->";
const END = "<!-- END TOOL CATALOG -->";

// One short, scanner-safe line of description.
function shortDesc(raw) {
  let s = String(raw)
    .replace(/\s+/g, " ")
    .replace(/[`]/g, "") // backticks -> nothing (avoid phantom-tool regex hits)
    .replace(/[—–]/g, "-") // em/en dash -> hyphen
    .trim();
  // Prefer the first sentence if it's reasonably short.
  const dot = s.indexOf(". ");
  if (dot > 0 && dot <= 60) s = s.slice(0, dot);
  // Hard cap at a word boundary.
  const CAP = 58;
  if (s.length > CAP) {
    s = s.slice(0, CAP);
    const sp = s.lastIndexOf(" ");
    if (sp > 30) s = s.slice(0, sp);
    s = s.replace(/[\s.,;:-]+$/, "") + "...";
  }
  return s;
}

const lines = toolDescriptors.map((t) => `- \`${t.name}\` - ${shortDesc(t.description)}`);
const count = lines.length;

const details = process.argv.includes("--details");
const inner = details
  ? `<details>\n<summary>${count} tools</summary>\n\n${lines.join("\n")}\n\n</details>`
  : lines.join("\n");
const block = `${BEGIN}\n${inner}\n${END}`;

if (process.argv.includes("--write")) {
  const readmePath = join(ROOT, "README.md");
  const readme = readFileSync(readmePath, "utf8");
  const re = new RegExp(`${escapeRe(BEGIN)}[\\s\\S]*?${escapeRe(END)}`);
  if (!re.test(readme)) {
    console.error("Marker block not found in README.md. Insert these two lines where the catalog should go:");
    console.error(`  ${BEGIN}\n  ${END}`);
    process.exit(1);
  }
  const next = readme.replace(re, block);
  writeFileSync(readmePath, next);
  const endOffset = next.indexOf(END) + END.length;
  console.error(`Wrote ${count} tools. END marker ends at raw char ${endOffset} (scanner cutoff = 8000; ${endOffset <= 8000 ? "OK" : "OVER BUDGET"}).`);
} else {
  process.stdout.write(block + "\n");
  process.stderr.write(`\n[${count} tools | block ${Buffer.byteLength(block)} bytes | details=${details}]\n`);
}

function escapeRe(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}
